// Print all the Odd numbers between 1 and 15.
console.log("Odd numbers");
for (let i = 1; i <= 15 ; i++){
    if(i%2 != 0){
        console.log(i);
    }
}

// Print all the Even numbers between 1 and 15.
console.log("Even numbers");
for (let j = 2; j <= 10; j++){
    if(j%2 == 0){
        console.log(j)
    }
}